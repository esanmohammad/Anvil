// ── Types ────────────────────────────────────────────────────────────────

interface RepoAnswers {
  name: string;
  path: string;
  github: string;
  language: string;
  build: string;
  test: string;
  lint: string;
  format: string;
}

interface InitAnswers {
  project: string;
  title: string;
  workspace: string;
  repos: RepoAnswers[];
  description: string;
  invariants: string[];
}

// ── Readline helpers ─────────────────────────────────────────────────────

function createRL(): ReturnType<typeof createInterface> {
  return createInterface({
    input: process.stdin,
    output: process.stderr, // prompts go to stderr so stdout stays clean
    terminal: true,
  });
}

function ask(rl: ReturnType<typeof createInterface>, question: string, defaultVal?: string): Promise<string> {
  const suffix = defaultVal ? ` ${pc.dim(`(${defaultVal})`)}` : '';
  return new Promise((resolve) => {
    rl.question(`${pc.cyan('?')} ${question}${suffix}: `, (answer) => {
      resolve(answer.trim() || defaultVal || '');
    });
  });
}

function askYesNo(rl: ReturnType<typeof createInterface>, question: string, defaultYes: boolean = false): Promise<boolean> {
  const hint = defaultYes ? 'Y/n' : 'y/N';
  return new Promise((resolve) => {
    rl.question(`${pc.cyan('?')} ${question} ${pc.dim(`(${hint})`)}: `, (answer) => {
      const a = answer.trim().toLowerCase();
      if (a === '') resolve(defaultYes);
      else resolve(a === 'y' || a === 'yes');
    });
  });
}

